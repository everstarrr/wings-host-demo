<script setup>
import { computed } from "vue";

const emit = defineEmits(["update:checked"]);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        default: false,
    },
    value: {
        type: String,
        default: null,
    },
    is_disabled: Boolean,
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit("update:checked", val);
    },
});
</script>

<template>
    <input
        :disabled="is_disabled"
        v-model="proxyChecked"
        type="checkbox"
        :value="value"
        class="rounded bg-neutral-900 border-neutral-700 text-wings shadow-sm focus:ring-0 focus:ring-offset-0" />
</template>
